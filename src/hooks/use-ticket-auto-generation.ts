import { useState } from 'react';
import { authenticatedPost, authenticatedPut } from '@/lib/utils/api';

export function useTicketAutoGeneration() {
  const [isGenerating, setIsGenerating] = useState(false);

  const triggerAutoGeneration = async (ticketId: string, attendeeData?: any) => {
    if (isGenerating) return;

    try {
      setIsGenerating(true);
      console.log('ğŸ¤– Triggering auto-generation for ticket:', ticketId);

      // 1. Si hay datos del asistente, guardarlos primero
      if (attendeeData) {
        console.log('ğŸ“‹ Saving attendee data first...');
        const saveResponse = await authenticatedPut(`/api/tickets/${ticketId}`, attendeeData); // âœ… PUT en lugar de POST
        
        if (!saveResponse.ok) {
          console.error('âŒ Failed to save attendee data');
          return null;
        }
        
        console.log('âœ… Attendee data saved, now generating PDF...');
      }

      // 2. Llamar API con flag autoGenerated
      const response = await authenticatedPost(`/api/tickets/${ticketId}/generate-pdf`, {
        autoGenerated: true
      });

      if (response.ok) {
        const result = await response.json();
        console.log('âœ… Auto-generation successful:', result);
        return result;
      } else {
        const error = await response.json();
        console.error('âŒ Auto-generation failed:', error);
        return null;
      }

    } catch (error) {
      console.error('âŒ Error in auto-generation:', error);
      // No lanzar error - la auto-generaciÃ³n es silenciosa
      return null;
    } finally {
      setIsGenerating(false);
    }
  };

  return {
    triggerAutoGeneration,
    isGenerating
  };
}
